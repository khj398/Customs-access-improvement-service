def main():
    rows = []
    with open(GT_FILE, encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        for row in reader:
            key = (row["pbac_no"], row["pbac_srno"], row["cmdt_ln_no"])
            if key in LABELS and not row.get("true_category_path", "").strip():
                true_path, labeler, note = LABELS[key]
                row["true_category_path"] = true_path
                row["labeler"] = labeler
                row["note"] = note
            rows.append(row)

    with open(GT_FILE, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    labeled = sum(1 for r in rows if r.get("true_category_path", "").strip())
    print(f"완료: 전체 {len(rows)}건 중 라벨 {labeled}건")


if __name__ == "__main__":
    main()
